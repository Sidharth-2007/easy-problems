// Find Smallest among 2 numbers

#include <iostream>
using namespace std;

int main(){
	int num1, num2;
	cout << "Enter 1st Number: " ;
	cin >> num1 ;
	cout << "Enter 2nd Number: " ;
	cin >> num2 ;
	if (num1 < num2 ) {
	    cout << "Smallest is : " << num1;
	}
	else {
	    cout << "Smallest is : " << num2;
	}
	
	return 0;
}
	
	
	
